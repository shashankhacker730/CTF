enc_flag = [
  0xE7, 0xE3, 0x72, 0x78, 0xAC, 0x90, 0x90, 0x7C, 0x90, 0xAC, 
  0xB1, 0xA6, 0xA4, 0x9E, 0xA7, 0xA2, 0xAC, 0x90, 0xB9, 0xB2, 
  0xBF, 0xBB, 0xBD, 0xB6, 0xAB, 0x90, 0xBA, 0xB4, 0x90, 0xBF, 
  0xC0, 0xC0, 0xC4, 0xCA, 0x95, 0xED, 0xC0, 0xB2
]

key1 = 0xBE
key2 = 0xEF



flag = enc_flag

for i in range(len(flag)):
  flag[i] -= i + key2
  flag[i] ^= i + key1

for i in range(len(flag)):
  flag[i] = flag[i] % 256



FLAG = "".join(map(chr,flag))
print(FLAG)